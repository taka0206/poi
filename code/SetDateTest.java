import java.io.*;
import java.util.*;
import org.apache.poi.hssf.usermodel.*;
import org.apache.poi.xssf.usermodel.*;
import org.apache.poi.ss.usermodel.*;

/**
 * 日付設定のテスト
 */
public class SetDateTest {

  /** 
   * 処理の実行
   * @param mode 動作モード
   */
  public void Run(String mode) {

    // ワークブックの生成
    Workbook workBook = mode.equals("2003") ? new HSSFWorkbook() : 
                                  new XSSFWorkbook();
    // シートの生成 
    Sheet sheet = workBook.createSheet();
    // CellStyle生成
    CellStyle style = workBook.createCellStyle();
    // 日付書式の設定
    DataFormat df = workBook.createDataFormat();
    style.setDataFormat(df.getFormat("yyyy年mm月dd日"));
    // 1列目は日付なので、ここで一括書式設定も可能
    //sheet.setDefaultColumnStyle(0, style);
    // Rowを5行、Cellを1列ずつ生成
    for (int i=0; i<5; i++) {
      sheet.createRow(i).createCell(0);
    }
    // 日付設定 当日(Date)
    sheet.getRow(0).getCell(0).setCellValue(new Date());
    sheet.getRow(0).getCell(0).setCellStyle(style);
    // 日付設定 当日(Calender)
    sheet.getRow(1).getCell(0).setCellValue(
        Calendar.getInstance());
    sheet.getRow(1).getCell(0).setCellStyle(style);
    sheet.autoSizeColumn(0); // 列幅自動設定にしておく
    // ワークブック書き出し
    FileOutputStream out = null;
    try{
      out = new FileOutputStream( mode.equals("2003") ? this.getClass().getName() + "_Book1.xls" : 
                      this.getClass().getName() + "_Book1.xlsx");
      workBook.write(out);
    }catch(IOException e){
      System.out.println("ブックの書き込みに失敗しました。\n" + e.toString());
    }finally{
      try {
        out.close();
      }catch(IOException e) {
        System.out.println("ブックの書き込みに失敗しました。\n" + e.toString());
      }
    }
    System.out.println("done!");
  }
  /** エントリーポイント */
  public static void main(String[] args) {
    if (args.length != 1) {
      System.out.println("エラー：モードを指定して下さい。");
      return;
    }
    else if ( !args[0].equals("2003") && !args[0].equals("2007") ) {
      System.out.println("エラー：モードは2003または2007を指定して下さい。");
      return;
    }
    // 処理の実行
    new SetDateTest().Run(args[0]);

    System.out.print("リターンキーで終了……");
    try {
      int c = System.in.read();
    }
    catch (Exception e) {
    }
  }
}
